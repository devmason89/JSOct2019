// Strictly Equal : best practice to use this 
3 === 3;

// Not equal
console.log(3 != 4);

// Strict not equal
console.log('3' !== 3);

// Greater than
3 > 2;

// Less than
2 < 3;

// Greater than or equal to
3 >= 2;

// Lesser than or equal to
2 <= 3;

// And
2 && 3; 

// Or
2 || 3 

